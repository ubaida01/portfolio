function setupCertificationsCarousel() {
  const track = document.getElementById("cert-track");
  const dots = document.getElementById("cert-dots");
  const carousel = document.getElementById("certifications-carousel");
  const items = portfolioData.certifications;
  let currentIndex = 0;
  let intervalId = null;

  track.innerHTML = items.map(createCertificationSlide).join("");

  function getVisibleSlides() {
    if (window.innerWidth >= 1024) {
      return 3;
    }
    if (window.innerWidth >= 768) {
      return 2;
    }
    return 1;
  }

  function getDotCount() {
    return Math.max(1, items.length - getVisibleSlides() + 1);
  }

  function buildDots() {
    dots.innerHTML = "";
    const count = getDotCount();
    for (let index = 0; index < count; index += 1) {
      const button = document.createElement("button");
      button.type = "button";
      button.className = index === currentIndex ? "is-active" : "";
      button.setAttribute("aria-label", `Go to certification slide ${index + 1}`);
      button.addEventListener("click", () => {
        currentIndex = index;
        updateCarousel();
        restartAutoplay();
      });
      dots.appendChild(button);
    }
  }

  function updateCarousel() {
    const visible = getVisibleSlides();
    const maxIndex = Math.max(0, items.length - visible);
    if (currentIndex > maxIndex) {
      currentIndex = 0;
    }

    carousel.style.setProperty("--visible-slides", String(visible));
    const slideWidth = 100 / visible;
    track.style.transform = `translateX(calc(-${currentIndex * slideWidth}% - ${currentIndex * 1.5}rem / ${visible}))`;

    dots.querySelectorAll("button").forEach((button, index) => {
      button.classList.toggle("is-active", index === currentIndex);
    });
  }

  function nextSlide() {
    const count = getDotCount();
    currentIndex = (currentIndex + 1) % count;
    updateCarousel();
  }

  function startAutoplay() {
    stopAutoplay();
    intervalId = window.setInterval(nextSlide, 1500);
  }

  function stopAutoplay() {
    if (intervalId) {
      window.clearInterval(intervalId);
      intervalId = null;
    }
  }

  function restartAutoplay() {
    startAutoplay();
  }

  buildDots();
  updateCarousel();
  startAutoplay();

  track.addEventListener("mouseenter", stopAutoplay);
  track.addEventListener("mouseleave", startAutoplay);
  window.addEventListener("resize", () => {
    buildDots();
    updateCarousel();
  });
}

function setupTypingEffect() {
  const target = document.getElementById("typing-text");
  const words = portfolioData.typingWords;
  let wordIndex = 0;
  let letterIndex = 0;
  let isDeleting = false;

  function tick() {
    const word = words[wordIndex];

    if (isDeleting) {
      letterIndex -= 1;
    } else {
      letterIndex += 1;
    }

    target.textContent = `${word.slice(0, letterIndex)}|`;

    let timeout = isDeleting ? 70 : 100;

    if (!isDeleting && letterIndex === word.length) {
      timeout = 1000;
      isDeleting = true;
    } else if (isDeleting && letterIndex === 0) {
      isDeleting = false;
      wordIndex = (wordIndex + 1) % words.length;
    }

    window.setTimeout(tick, timeout);
  }

  tick();
}

