function setupNavState() {
  const nav = document.getElementById("site-nav");
  const sections = Array.from(document.querySelectorAll("main section"));
  const links = Array.from(document.querySelectorAll("[data-link]"));

  const observer = new IntersectionObserver(
    (entries) => {
      const active = entries.find((entry) => entry.isIntersecting);
      if (!active) {
        return;
      }

      const id = active.target.id;
      links.forEach((link) => {
        link.classList.toggle("is-active", link.dataset.link === id);
      });
    },
    { threshold: 0.3 }
  );

  sections.forEach((section) => observer.observe(section));

  function onScroll() {
    nav.classList.toggle("is-scrolled", window.scrollY > 20);
  }

  onScroll();
  window.addEventListener("scroll", onScroll, { passive: true });
}

function setupRevealAnimations() {
  const reveals = document.querySelectorAll(".reveal");
  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("is-visible");
        } else {
          entry.target.classList.remove("is-visible");
        }
      });
    },
    { threshold: 0.2 }
  );

  reveals.forEach((element) => observer.observe(element));
}

function setupScrollTop() {
  const button = document.getElementById("scroll-top");
  const progressCircle = document.getElementById("scroll-progress");
  const radius = 20;
  const circumference = 2 * Math.PI * radius;

  progressCircle.style.strokeDasharray = `${circumference}`;
  progressCircle.style.strokeDashoffset = `${circumference}`;

  function onScroll() {
    const scrollTop = window.scrollY;
    const height = document.documentElement.scrollHeight - window.innerHeight;
    const progress = height > 0 ? Math.min(scrollTop / height, 1) : 0;

    button.classList.toggle("is-visible", scrollTop > 300);
    progressCircle.style.strokeDashoffset = `${circumference * (1 - progress)}`;
  }

  button.addEventListener("click", () => {
    window.scrollTo({ top: 0, behavior: "smooth" });
  });

  onScroll();
  window.addEventListener("scroll", onScroll, { passive: true });
}

function setupContactForm() {
  const form = document.getElementById("contact-form");
  const submitButton = document.getElementById("submit-button");
  const status = document.getElementById("form-status");

  form.addEventListener("submit", async (event) => {
    event.preventDefault();
    status.textContent = "";
    status.className = "form-status";
    submitButton.textContent = "Sending...";
    submitButton.disabled = true;

    const formData = {
      name: form.name.value,
      email: form.email.value,
      message: form.message.value,
    };

    try {
      const response = await fetch(portfolioData.formAction, {
        method: "POST",
        headers: {
          "Content-Type": "application/json",
        },
        body: JSON.stringify(formData),
      });

      if (!response.ok) {
        const error = await response.json().catch(() => ({}));
        throw new Error(error.error || "Something went wrong. Please try again.");
      }

      form.reset();
      status.innerHTML = "Got your message !<br>(I actually read every one)";
      status.classList.add("is-success");
    } catch (error) {
      status.textContent = error.message || "An unexpected error occurred. Please try again.";
      status.classList.add("is-error");
    } finally {
      submitButton.textContent = "Send Message";
      submitButton.disabled = false;
    }
  });
}

